package net.octyl.clockresonator.app.jackson;

import com.cronutils.model.Cron;
import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.SerializerProvider;
import com.fasterxml.jackson.databind.ser.std.StdSerializer;

import java.io.IOException;

public class CronSerializer extends StdSerializer<Cron> {
    protected CronSerializer() {
        super(Cron.class);
    }

    @Override
    public void serialize(Cron value, JsonGenerator gen, SerializerProvider provider) throws IOException {
        gen.writeString(value.asString());
    }
}
